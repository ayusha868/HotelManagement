    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String roomNoStr = JOptionPane.showInputDialog(this, "Enter Room Number:");
        String roomType = JOptionPane.showInputDialog(this, "Enter Room Type (Single/Double/Suite):");
        String bedsStr = JOptionPane.showInputDialog(this, "Enter Number of Beds:");
        String priceStr = JOptionPane.showInputDialog(this, "Enter Price Per Night:");
        
        if (roomNoStr == null || roomType == null || bedsStr == null || priceStr == null) {
            return; // cancelled
        }
        
        try {
            int roomNo = Integer.parseInt(roomNoStr.trim());
            int beds = Integer.parseInt(bedsStr.trim());
            double price = Double.parseDouble(priceStr.trim());
            
            if (HotelData.findRoomByNumber(roomNo) != null) {
                JOptionPane.showMessageDialog(this, "Room number already exists!");
                return;
            }
            
            Room newRoom = new Room(roomNo, roomType.trim(), beds, price, "Available");
            HotelData.addRoom(newRoom);
            
            JOptionPane.showMessageDialog(this, "Room added successfully!");
            loadDashboardData(); // refresh stats
            
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Please enter valid numbers for Room No, Beds, and Price.");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

