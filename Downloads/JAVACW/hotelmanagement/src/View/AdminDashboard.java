    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Please select a booking to delete.");
            return;
        }
        
        int bookingId = (Integer) jTable1.getValueAt(selectedRow, 0);
        int confirm = JOptionPane.showConfirmDialog(this, "Delete booking ID " + bookingId + "?", "Confirm Delete", JOptionPane.YES_NO_OPTION);
        
        if (confirm == JOptionPane.YES_OPTION) {
            HotelData.deleteBooking(bookingId);
            JOptionPane.showMessageDialog(this, "Booking deleted successfully!");
            refreshBookingsTable();
            loadDashboardData(); // update stats
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        List<Model.Booking> bookings = HotelData.getAllBookings();
        
        Collections.sort(bookings, new Comparator<Model.Booking>() {
            public int compare(Model.Booking b1, Model.Booking b2) {
                return Integer.compare(b1.getRoomNo(), b2.getRoomNo());
            }
        });
        
        model.setRowCount(0);
        for (Model.Booking b : bookings) {
            model.addRow(new Object[]{b.getBookingId(), b.getRoomNo(), b.getCustomerName(), 
                                     b.getCheckInDate(), b.getCheckOutDate(), b.getStatus()});
        }
    }//GEN-LAST:event_jButton4ActionPerformed

